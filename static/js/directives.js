//Bootstrap Carousel
function bootstrapCarouselDirective() {
  var directive = {
    restrict: 'E',
    link: link
  };
  return directive;

  function link(scope, element, attrs) {
    if (attrs.ride=='carousel'){
      element.find('a').each(function(){
        $(this).attr('data-target',$(this).attr('href').replace('index.html','')).attr('href','javascript;;')
      });
    }
  }
}

//Bootstrap Tooltips & Popovers
function bootstrapTooltipsPopoversDirective() {
  var directive = {
    restrict: 'A',
    link: link
  };
  return directive;

  function link(scope, element, attrs) {
    if (attrs.toggle=='tooltip'){
      angular.element(element).tooltip();
    }
    if (attrs.toggle=='popover'){
      angular.element(element).popover();
    }
  }
}

//Bootstrap Tabs
function bootstrapTabsDirective() {
  var directive = {
    restrict: 'A',
    link: link
  };
  return directive;

  function link(scope, element, attrs) {
    element.click(function(e) {
      e.preventDefault();
      angular.element(element).tab('show');
    });
  }
}

//Card Collapse
function cardCollapseDirective() {
  var directive = {
    restrict: 'E',
    link: link
  };
  return directive;

  function link(scope, element, attrs) {
    if (attrs.toggle=='collapse' && element.parent().hasClass('card-actions')){

      if (element.parent().parent().parent().find('.card-body').hasClass('in')) {
        element.find('i').addClass('r180');
      }

      var id = 'collapse-' + Math.floor((Math.random() * 1000000000) + 1);
      element.attr('data-target','#'+id)
      element.parent().parent().parent().find('.card-body').attr('id',id);

      element.on('click', function(){
        element.find('i').toggleClass('r180');
      })
    }
  }
}

Application.directive('focus', function () {
    return {
        restrict: 'A',
        link: function ($scope, elem, attrs, ctrls) {
            elem.bind('keydown', function (e) {
                var code = e.keyCode || e.which;
                if (code === 13) {

                    e.preventDefault();
                    nextElementCandidate = attrs.focus.split("|");
                    //console.log('call 1 ' + nextElementCandidate);
                    element = null;
                    i = 0;
                    while (element === null && nextElementCandidate.length !== 0 && i < nextElementCandidate.length) {
                        element = document.getElementById(nextElementCandidate[i]);
                        //console.log('call 2 ' + element);
                        if (element) {
                            if (!$(element).is(":visible") || $(element).prop("disabled") === true) {
                                element = null;
                            } else {
                                if (element.tagName === 'INPUT' || element.tagName === 'SELECT') {
                                    //console.log(element);
                                    element.focus();
                                } else if (element.tagName === 'BUTTON') {
                                    //console.log('call 3 ' + element);
                                    $(element).click();
                                }
                            }
                        }
                        i++;
                    }
                }
            });
        }
    };
});

Application.directive('numericOnly', function () {
    return {
        require: 'ngModel',
        link: function (scope, element, attrs, modelCtrl) {

            modelCtrl.$parsers.push(function (inputValue) {
                var transformedInput = inputValue.replace(/[^\d.-]/g, '');

                if (transformedInput != inputValue) {
                    modelCtrl.$setViewValue(transformedInput);
                    modelCtrl.$render();
                }

                return transformedInput;
            });
        }
    };
});

Application.directive('focusMe', function ($timeout) {
    return function (scope, element, attrs) {
        attrs.$observe('focusMe', function (value) {
            if (value === "true") {
                $timeout(function () {
                    element[0].focus();
                }, 5);
            }
        });
    };
});

